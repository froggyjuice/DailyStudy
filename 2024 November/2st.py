# 학교 급식실 시나리오
오늘의_메뉴 = "김치찌개"
식수_인원 = 100
배식_현황 = {
    "조리완료": ["김치찌개", "된장찌개"],
    "배식중": [],
    "배식완료": []
}

def 메뉴_변경(menu, count, status):
    # 불변 객체 변경 시도
    menu = "된장찌개"
    count = 150
    # 가변 객체 새로운 할당
    status = {
        "조리완료": ["된장찌개"],
        "배식중": [],
        "배식완료": []
    }
    print(f"[메뉴 변경 함수 내부]")
    print(f"메뉴: {menu}")
    print(f"식수: {count}")
    print(f"현황: {status}")

def 배식_시작(menu, count, status):
    # 불변 객체 변경 시도
    menu = "비빔밥"
    count += 10
    # 가변 객체 수정
    첫번째_메뉴 = status["조리완료"].pop(0)  # 첫 번째 메뉴 제거
    status["배식중"].append(첫번째_메뉴)     # 배식중으로 이동
    print(f"[배식 시작 함수 내부]")
    print(f"메뉴: {menu}")
    print(f"식수: {count}")
    print(f"현황: {status}")

def 배식_완료(menu, count, status):
    # 딕셔너리의 리스트 조작
    배식중_메뉴 = status["배식중"].pop(0)    # 배식중 메뉴 제거
    status["배식완료"].append(배식중_메뉴)   # 배식완료로 이동
    return status.copy()  # 현황 복사본 반환

print("\n=== 처음 상태 ===")
print(f"메뉴: {오늘의_메뉴}")
print(f"식수: {식수_인원}")
print(f"현황: {배식_현황}")

print("\n=== 메뉴 변경 시도 ===")
메뉴_변경(오늘의_메뉴, 식수_인원, 배식_현황)
print("\n=== 메뉴 변경 후 상태 ===")
print(f"메뉴: {오늘의_메뉴}")
print(f"식수: {식수_인원}")
print(f"현황: {배식_현황}")

print("\n=== 배식 시작 ===")
배식_시작(오늘의_메뉴, 식수_인원, 배식_현황)
print("\n=== 배식 시작 후 상태 ===")
print(f"메뉴: {오늘의_메뉴}")
print(f"식수: {식수_인원}")
print(f"현황: {배식_현황}")

print("\n=== 배식 완료 ===")
새로운_현황 = 배식_완료(오늘의_메뉴, 식수_인원, 배식_현황)
print("\n=== 배식 완료 후 상태 ===")
print(f"메뉴: {오늘의_메뉴}")
print(f"식수: {식수_인원}")
print(f"원본 현황: {배식_현황}")
print(f"복사된 현황: {새로운_현황}")
